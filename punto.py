class Punto():
    #Constructor de la clase Punto
    def __init__(self,x,y):
        self.x = x
        self.y = y
    
    def get_x(self):
        return self.x

    def get_y(self):
        return self.y

    def set_x(self,cordX):
        self.x = cordX

    def set_y(self,cordY):
        self.y = cordY
